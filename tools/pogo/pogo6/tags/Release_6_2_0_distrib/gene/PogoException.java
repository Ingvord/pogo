package pogo.gene;


public class PogoException extends Exception
{
	public String[]	str;

	public PogoException(String s)
	{
		str = new String[1];
		str[0] = s;
    }
	public PogoException(String s1, String s2)
	{
		str = new String[2];
		str[0] = s1;
		str[1] = s2;
    }
	public String toString()
	{
		StringBuffer	sb = new StringBuffer("PogoException:\n");
		for (int i=0 ; i<str.length ; i++)
		{
			sb.append(str[i]);
			if (i<str.length-1)
				sb.append("\n");
		}
		return sb.toString();
	}
}
